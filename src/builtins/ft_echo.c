#include "../../headers/minishell.h"

int	ft_echo(t_cmd	*cmd)
{
	if (cmd->args == NULL)
		printf("\n");
	else
		printf("%s\n", cmd->args);
	if (!(cmd->flag))
		printf("\n");
	return (0);
}
